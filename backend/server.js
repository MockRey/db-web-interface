// –≠–Ω–¥–ø–æ–∏–Ω—Ç –¥–ª—è —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–∏ play_history –∏ –ø–µ—Ä–µ–¥–∞—á–∏ –¥–∞–Ω–Ω—ã—Ö –ø–æ –∏–≥—Ä–æ–∫—É
app.post('/player-stats', async (req, res) => {
    const { playerId, startDate, endDate, game } = req.body;
  
    try {
      // –°–Ω–∞—á–∞–ª–∞ –ø—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ —Ç–∞–∫–æ–π –∏–≥—Ä–æ–∫
      const playerCheck = await pool.query('SELECT * FROM players WHERE player_id = $1', [playerId]);
      
      if (playerCheck.rowCount === 0) {
        return res.status(404).json({ error: '–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π ID –∏–≥—Ä–æ–∫–∞' });
      }
  
      // –°—Ç—Ä–æ–∏–º –∑–∞–ø—Ä–æ—Å –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏
      let query = `
        SELECT play_history.*, levels.game_id, levels.max_points, levels.min_points
        FROM play_history
        JOIN levels ON play_history.level_id = levels.level_id
        WHERE play_history.player_id = $1
      `;
      const params = [playerId];
      let paramIndex = 2; // —Å—á—ë—Ç—á–∏–∫ –∏–Ω–¥–µ–∫—Å–æ–≤, 2 - —ç—Ç–æ —Å–ª–µ–¥—É—é—â–∏–π –∏–Ω–¥–µ–∫—Å –ø–æ—Å–ª–µ $1

      // –î–æ–±–∞–≤–ª—è–µ–º —Ñ–∏–ª—å—Ç—Ä –ø–æ startDate, –µ—Å–ª–∏ –æ–Ω –ø–µ—Ä–µ–¥–∞–Ω
        if (startDate) {
            query += ` AND play_history.start_time >= $${paramIndex}`;
            params.push(startDate);
            paramIndex++;
        }

      // –î–æ–±–∞–≤–ª—è–µ–º —Ñ–∏–ª—å—Ç—Ä –ø–æ endDate, –µ—Å–ª–∏ –æ–Ω –ø–µ—Ä–µ–¥–∞–Ω
        if (endDate) {
            query += ` AND play_history.end_time <= $${paramIndex}`;
            params.push(endDate);
            paramIndex++;
        }
  
      // –ü—Ä–æ–≤–µ—Ä–∫–∞ –≤—ã–±—Ä–∞–Ω–Ω–æ–π –∏–≥—Ä—ã (–µ—Å–ª–∏ –Ω–µ 'all', —Ç–æ –¥–æ–±–∞–≤–ª—è–µ–º —É—Å–ª–æ–≤–∏–µ)
      if (game !== 'all') {
        query += ` AND levels.game_id = $${paramIndex}`;
        params.push(game);
      }

      const result = await pool.query(query, params);
  
      if (result.rowCount === 0) {
        return res.status(404).json({ error: '–ù–µ—Ç –¥–∞–Ω–Ω—ã—Ö –ø–æ —É–∫–∞–∑–∞–Ω–Ω—ã–º —É—Å–ª–æ–≤–∏—è–º' });
      }
  
      res.json({ stats: result.rows });
    } 
    
    catch (err) {
      console.error('‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –∏–≥—Ä–æ–∫–∞:', err);
      res.status(500).json({ error: '–û—à–∏–±–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –∏–≥—Ä–æ–∫–∞' });
    }
  });
  